        User::create([
            'name' => 'Admin User',
            'email' => 'admin@example.com',
            'password' => Hash::make('password'),
            'role' => 'admin',
            'status' => 'active'
        ]);

        // Create 10 employee users
        User::factory()
            ->count(10)
            ->state(['role' => 'employee'])
            ->create();
